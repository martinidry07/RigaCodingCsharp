using System;
using System.Collections.Generic;
using System.Text;

namespace Day05
{
    class MyCalculator
    {
        public static double numCounting (double num1, double num2)
        {
            return (num1 + num2);
        }
        public static double numSubtract(double num1, double num2)
        {
            return (num1 - num2);
        }
        public static double numMultiplay(double num1, double num2)
        {
            return (num1 * num2);
        }
        public static double numDivide(double num1, double num2)
        {
            return (num1 / num2);
        }
    }
}
